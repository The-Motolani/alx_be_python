# a script that converts a specific number of hours into seconds.
# Define the variable
hours = 2  # Number of hours to convert
# Calculate the number of seconds in the given hours
# There are 3600 seconds in one hour
# 1 hour = 60 minutes * 60 seconds = 3600 seconds
seconds = hours * 3600
# Print the result
print(f"{hours} hour(s) is {seconds} seconds.")